public class Switch{
    public static void main(String args[]){
        for(int i=0;i<4;i++){
            switch(i){
                case 1:
                    System.out.println("case 1");
                    break;
                case 2:
                    System.out.println("case 2");
                case 3:
                    System.out.println("case 3");
                    break;
            }
        }
    }
}